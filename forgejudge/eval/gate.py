"""Regression gates (spec decision #12).

Two distinct gates live here, because they guard two *different* variance axes:

1. ``regression_gate`` / ``mean_ci`` — the STOCHASTIC multi-seed sweep. A model
   swap / scaffold change should only be *blocked* when it is statistically —
   not merely noisily — worse than the baseline. We run each side over several
   SEEDS, turn the per-seed resolution rates into a small-sample confidence
   interval (mean +/- t * standard error, Student-t for the realistic n=3-5
   seed counts), and fail IFF the candidate's upper bound sits strictly below
   the baseline's lower bound. Equal, overlapping, flaky-but-overlapping, or
   improved distributions all pass. The samples MUST be per-seed reruns of the
   SAME task set (genuine run-to-run noise), never partitions of one run.

2. ``exact_gold_gate`` — the DETERMINISTIC gold/harness-integrity gate. Grading
   the gold patches is $0 and deterministic, so the per-shard rates are a
   partition of ONE run, not noisy seeds; a CI over them is meaningless. Here we
   pool ALL shard records into one overall resolution rate and require it to be
   exactly 1.0 (every gold task must resolve). Any partial breakage fails.

Pure standard library: no scipy/numpy. ``mean_ci`` uses a small vendored
Student-t table for n-1 dof (the realistic seed counts) and a normal
approximation only for large n.

Baseline provenance (``eval/baseline_scores.json``)
---------------------------------------------------
The committed baseline is a real MULTI-SEED sample (>= 3 per-seed resolution
rates of the reference agent over the golden set), not a single placeholder
point — a single point has no variance estimate and makes ``regression_gate``
statistically unsound (see ``regression_gate``'s >= 2-seed guard). It is
regenerated by running the stochastic sweep over several seeds and recording one
resolution rate per seed, e.g.::

    python -m forgejudge.eval.sweep --model <ref-model> --seeds 0,1,2 --no-store
    # take SweepResult.resolution_rate per seed -> one float per seed

and writing the resulting list of floats to ``eval/baseline_scores.json``.
"""

import argparse
import json
import math
import os
import statistics
import sys
from dataclasses import dataclass
from pathlib import Path

# Two-sided 97.5% Student-t critical values by degrees of freedom (n-1). These
# cover the realistic seed counts (n=2..11); for larger n we fall back to the
# normal z=1.96, which is then a good approximation. statistics.stdev is the
# SAMPLE (n-1) stdev, so the correct multiplier for a small-sample CI of a mean
# is the Student-t critical value, NOT the normal z (which is too narrow for
# small n and makes the gate over-eager to FAIL ordinary seed noise).
_T_975: dict[int, float] = {
    1: 12.706,
    2: 4.30265,
    3: 3.18245,
    4: 2.77645,
    5: 2.57058,
    6: 2.44691,
    7: 2.36462,
    8: 2.30600,
    9: 2.26216,
    10: 2.22814,
}
_Z_975 = 1.96  # large-sample normal approximation (n > 30)


def _t_critical(dof: int) -> float:
    """Two-sided 97.5% critical value for ``dof`` degrees of freedom."""
    if dof in _T_975:
        return _T_975[dof]
    # For dof beyond the table the normal approximation is close enough.
    return _Z_975


def mean_ci(scores: list[float]) -> tuple[float, float, float]:
    """Return ``(mean, lower, upper)`` for a list of per-seed scores.

    The interval is ``mean +/- t * (sample_stdev / sqrt(n))`` where ``t`` is the
    two-sided 97.5% Student-t critical value for ``n-1`` degrees of freedom — the
    statistically correct multiplier for a small-sample CI of a mean. With fewer
    than two samples there is no spread to estimate, so we degenerate to a
    zero-width interval at the mean. An empty list is treated as mean ``0.0``
    (also a zero-width interval), so a missing side never crashes the gate.

    Note: a single-sample (n=1) degenerate interval is statistically
    meaningless; :func:`regression_gate` rejects single-sample sides outright
    rather than acting on one. ``mean_ci`` itself stays total for low-level use.
    """
    if not scores:
        return 0.0, 0.0, 0.0
    mean = statistics.mean(scores)
    n = len(scores)
    if n < 2:
        # Single seed: no variance estimate available, so the CI collapses to
        # the point estimate rather than fabricating a spread.
        return mean, mean, mean
    stderr = statistics.stdev(scores) / math.sqrt(n)
    half = _t_critical(n - 1) * stderr
    return mean, mean - half, mean + half


@dataclass
class GateResult:
    """Verdict of the regression gate for one candidate vs one baseline."""

    passed: bool
    detail: str
    baseline_mean: float
    candidate_mean: float
    baseline_lower: float
    candidate_upper: float


def regression_gate(
    baseline_scores: list[float],
    candidate_scores: list[float],
) -> GateResult:
    """Decide whether ``candidate`` is a statistically real STOCHASTIC regression.

    ``*_scores`` are per-SEED resolution rates (each in ``[0, 1]``) — independent
    reruns of the SAME task set, capturing genuine run-to-run agent noise. (Do
    NOT pass task-shard partitions of one run here; see :func:`exact_gold_gate`.)
    We build a small-sample (Student-t) CI for each side and FAIL only when the
    candidate's *upper* bound is strictly below the baseline's *lower* bound —
    i.e. even being generous to the candidate and harsh to the baseline, the
    candidate still loses. Equal, overlapping, or improved distributions pass;
    one flaky low seed that widens the candidate CI enough to overlap does not
    fail.

    A side with exactly ONE sample has no variance estimate, so a verdict from
    it would be statistically meaningless (the candidate's optimistic point
    estimate would be compared against the baseline's point estimate). We refuse
    rather than silently pass/fail: any non-empty side must carry >= 2 seeds.

    Both sides empty is a contract violation and raises ``ValueError``; a single
    *empty* side is tolerated (treated as a mean of ``0.0`` — e.g. the candidate
    crashed and produced nothing, which should regress).
    """
    if not baseline_scores and not candidate_scores:
        raise ValueError("regression_gate: both score lists are empty")
    if len(baseline_scores) == 1:
        raise ValueError(
            "regression_gate: baseline has a single seed; >= 2 seeds are required "
            "for a variance estimate (commit a real multi-seed baseline)"
        )
    if len(candidate_scores) == 1:
        raise ValueError(
            "regression_gate: candidate has a single seed; >= 2 seeds are required "
            "for a variance estimate (run the sweep over >= 2 seeds)"
        )

    baseline_mean, baseline_lower, _baseline_upper = mean_ci(baseline_scores)
    candidate_mean, _candidate_lower, candidate_upper = mean_ci(candidate_scores)

    passed = not (candidate_upper < baseline_lower)
    if passed:
        verdict = (
            f"PASS: candidate CI upper {candidate_upper:.4f} is not below "
            f"baseline CI lower {baseline_lower:.4f}; the intervals overlap or "
            f"the candidate is better (no statistically significant regression)"
        )
    else:
        verdict = (
            f"FAIL: candidate CI upper {candidate_upper:.4f} < baseline CI "
            f"lower {baseline_lower:.4f}; the candidate is statistically worse "
            f"(regression, not noise)"
        )
    detail = (
        f"{verdict}. baseline mean={baseline_mean:.4f} lower={baseline_lower:.4f}; "
        f"candidate mean={candidate_mean:.4f} upper={candidate_upper:.4f}"
    )
    return GateResult(
        passed=passed,
        detail=detail,
        baseline_mean=baseline_mean,
        candidate_mean=candidate_mean,
        baseline_lower=baseline_lower,
        candidate_upper=candidate_upper,
    )


def scores_from_run_artifacts(artifacts_dir: str | Path) -> list[float]:
    """Per-FILE resolution rates from RunRecord ``*.jsonl`` files (one score each).

    WARNING: each file is one shard/seed file, so this returns one rate PER FILE.
    For the deterministic gold gate the shards partition ONE run, so these are
    NOT independent seeds — use :func:`resolution_rate_from_run_artifacts` /
    :func:`exact_gold_gate` there. Reserve passing this list to
    :func:`regression_gate` for the stochastic sweep, where each file is a
    genuine independent SEED rerun of the full task set.
    """
    scores: list[float] = []
    for p in sorted(Path(artifacts_dir).rglob("*.jsonl")):
        recs = [json.loads(line) for line in p.read_text().splitlines() if line.strip()]
        if recs:
            scores.append(sum(1 for r in recs if r.get("resolved")) / len(recs))
    return scores


def resolution_rate_from_run_artifacts(artifacts_dir: str | Path) -> tuple[float, int, int]:
    """Pool ALL RunRecord ``*.jsonl`` records into one ``(rate, resolved, total)``.

    Used by the deterministic gold gate: the shard files partition a single
    deterministic run, so the correct aggregate is one overall resolution rate
    over every gold task, not a per-shard CI. ``rate`` is ``0.0`` when there are
    no records.
    """
    resolved = 0
    total = 0
    for p in sorted(Path(artifacts_dir).rglob("*.jsonl")):
        for line in p.read_text().splitlines():
            if not line.strip():
                continue
            rec = json.loads(line)
            total += 1
            if rec.get("resolved"):
                resolved += 1
    rate = resolved / total if total else 0.0
    return rate, resolved, total


def exact_gold_gate(artifacts_dir: str | Path) -> GateResult:
    """Deterministic harness-integrity gate over the pooled gold grading run.

    Gold grading is $0 and deterministic, so the shards are a partition of ONE
    run — a CI over them is meaningless. We pool every record into one overall
    resolution rate and PASS only when it is exactly ``1.0`` (every gold task
    resolved). Any partial breakage — even a single unresolved task — FAILS, and
    empty artifacts (nothing graded) FAIL too. This is the gate that actually
    blocks a change that breaks patch application / grading.
    """
    rate, resolved, total = resolution_rate_from_run_artifacts(artifacts_dir)
    passed = total > 0 and resolved == total
    if total == 0:
        detail = (
            "FAIL: no gold grading records found in artifacts; the harness "
            "produced nothing to verify (every gold task must resolve)"
        )
    elif passed:
        detail = (
            f"PASS: all {total} gold tasks resolved (rate {rate:.4f}); harness "
            f"integrity intact"
        )
    else:
        detail = (
            f"FAIL: only {resolved}/{total} gold tasks resolved (rate {rate:.4f}); "
            f"gold grading is deterministic so every task must resolve — a partial "
            f"failure is a real harness/patch-application regression"
        )
    return GateResult(
        passed=passed,
        detail=detail,
        baseline_mean=1.0,
        candidate_mean=rate,
        baseline_lower=1.0,
        candidate_upper=rate,
    )


def main() -> None:
    ap = argparse.ArgumentParser(description="ForgeJudge regression gates")
    ap.add_argument(
        "--mode",
        choices=["seed", "exact"],
        default="seed",
        help="'seed': stochastic multi-seed CI gate; 'exact': deterministic gold gate",
    )
    ap.add_argument("--baseline", help="JSON file: list of baseline per-seed scores (seed mode)")
    ap.add_argument("--candidate", help="JSON file: list of candidate per-seed scores (seed mode)")
    ap.add_argument(
        "--candidate-runs",
        help="dir of RunRecord *.jsonl: per-seed files (seed mode) or gold shards (exact mode)",
    )
    args = ap.parse_args()

    if args.mode == "exact":
        if not args.candidate_runs:
            ap.error("--mode exact requires --candidate-runs")
        result = exact_gold_gate(args.candidate_runs)
    else:
        if not args.baseline:
            ap.error("--mode seed requires --baseline")
        baseline = json.loads(Path(args.baseline).read_text())
        if args.candidate:
            candidate = json.loads(Path(args.candidate).read_text())
        elif args.candidate_runs:
            candidate = scores_from_run_artifacts(args.candidate_runs)
        else:
            ap.error("provide --candidate or --candidate-runs")
        result = regression_gate(baseline, candidate)

    print(result.detail)
    summary = os.getenv("GITHUB_STEP_SUMMARY")
    if summary:
        badge = "✅ PASS" if result.passed else "❌ FAIL"
        title = "Gold integrity gate" if args.mode == "exact" else "Regression gate"
        with Path(summary).open("a") as fh:
            fh.write(f"### {title}: {badge}\n\n```\n{result.detail}\n```\n")
    sys.exit(0 if result.passed else 1)


if __name__ == "__main__":
    main()
